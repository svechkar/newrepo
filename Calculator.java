package com.company;

public class Calculator {
    public int getSum(int a, int b) {
        return  a + b;
    }

    public int getDif(int a, int b) {
        return  a - b;
    }

    public int getMul(int a, int b) {
        return  a * b;
    }

    public int getDiv(int a, int b) {
        return  a / b;
    }

}

